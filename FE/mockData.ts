export const generateGlobalInsights = (projects: Project[]): AIInsight[] => {
  const insights: AIInsight[] = [];
  
  // 1. High Risk Volume Insight
  const highRiskCount = projects.filter(p => p.riskLevel === RiskLevel.HIGH).length;
  if (highRiskCount > 5) {
    insights.push({
      id: 'ai-1',
      title: `${highRiskCount} dự án đang ở mức Rủi ro Cao`,
      description: 'Số lượng dự án trễ hạn > 7 ngày hoặc có nhiều lỗi nghiêm trọng đang tăng đột biến so với tuần trước.',
      category: InsightCategory.SCHEDULE,
      severity: InsightSeverity.HIGH,
      actionLabel: 'Xem danh sách rủi ro'
    });
  }

  // 2. People Bottleneck Insight
  // Find Impl Rep with most active projects
  const workload: Record<string, number> = {};
  projects.filter(p => p.status === ProjectStatus.IN_PROGRESS).forEach(p => {
    workload[p.implRep] = (workload[p.implRep] || 0) + 1;
  });
  const busiestRep = Object.entries(workload).sort((a,b) => b[1] - a[1])[0];
  if (busiestRep && busiestRep[1] > 5) {
    insights.push({
      id: 'ai-2',
      title: `${busiestRep[0]} đang quá tải (${busiestRep[1]} dự án)`,
      description: 'Nhân sự này đang phụ trách số lượng dự án active cao gấp 2 lần trung bình team.',
      category: InsightCategory.PEOPLE,
      severity: InsightSeverity.MEDIUM,
      actionLabel: 'Điều phối lại'
    });
  }

  // 3. Client Pattern Insight
  const complaints = projects.filter(p => p.complaints > 0).length;
  if (complaints > 0) {
    insights.push({
      id: 'ai-3',
      title: 'Xu hướng phàn nàn từ nhóm Khách hàng',
      description: 'Có sự gia tăng các phàn nàn liên quan đến tiến độ triển khai trong tháng này.',
      category: InsightCategory.CLIENT,
      severity: InsightSeverity.MEDIUM,
    });
  }

  // 4. Financial Insight
  const totalRemaining = projects.reduce((acc, p) => acc + (p.valueSigned - p.revenueRecognized), 0);
  insights.push({
    id: 'ai-4',
    title: `Dòng tiền chờ thu: ${(totalRemaining/1000000000).toFixed(1)} tỷ VND`,
    description: 'Tập trung nghiệm thu các dự án đang ở giai đoạn cuối để đẩy nhanh dòng tiền tháng này.',
    category: InsightCategory.FINANCIAL,
    severity: InsightSeverity.LOW,
  });

  return insights;
};

